import express, { Router } from "express";

import { auth } from "@dinedrop/shared";
import { riderController, riderValidation } from "../../modules/rider";
import { validate } from "@dinedrop/shared";

const router: Router = express.Router();

router.route("/near-by-riders").get(riderController.getNearByRiders);

router
  .route("/")
  .post(
    auth("manageRiders"),
    validate(riderValidation.createRider),
    riderController.createRider
  )
  .get(auth(), validate(riderValidation.getRiders), riderController.getRiders);

router
  .route("/:riderId")
  .get(
    auth("getRiders"),
    validate(riderValidation.getRiderById),
    riderController.getRider
  )
  .patch(
    auth("manageRiders"),
    validate(riderValidation.updateRider),
    riderController.updateRider
  )
  .delete(
    auth("manageRiders"),
    validate(riderValidation.deleteRider),
    riderController.deleteRider
  );

export default router;

